def sumar(a,b):
    return ("La suma es: " + str((a + b)))
    
def restar(a,b):
    return ("La resta es: " +  str((a - b)))

def multiplicar(a,b):
    return ("La multiplicación es: " +  str((a * b)))
    
    
def dividir(a,b):
    return ("La división es: " +  str((a / b)))


print(sumar(5,5))
print(restar(5,5))
print(multiplicar(5,5))
print(dividir(5,5))


